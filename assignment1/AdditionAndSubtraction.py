#Ask the user to input two numbers (num1 and num2).
print("Addition and Subtraction of Two Numbers")
num1 = int(input("Provide the first number: "))
num2 = int(input("Provide the second number: "))

# Add the two numbers together to find the sum.
sum = num1 + num2

# Subtract the two numbers to find the difference.
difference = num1 - num2

#print the output
print(num1,  "+", num2, "=", sum)
print(num1, "-", num2, "=", difference)