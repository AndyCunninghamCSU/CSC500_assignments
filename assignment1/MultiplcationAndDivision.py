# Ask the user to input two numbers (num1 and num2).
print("Multiplication and Division of Two Numbers")
num1 = int(input("Provide the first number: "))
num2 = int(input("Provide the second number: "))

# Multiply the two numbers to find the product.
product = num1 * num2

# Divide num1 by num2 to find the quotient. 
quotient = num1 / num2

# Print the Output
print(num1,  "*", num2, "=", product)
print(num1, "/", num2, "=", quotient)

